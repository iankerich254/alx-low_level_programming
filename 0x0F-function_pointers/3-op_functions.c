#include "3-calc.h"
/**
 * op_add - function to add
 * @a: first parameter
 * @b: second parameter
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to subtract
 * @a: first parameter
 * @b: second parameter
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function
 * @a: first paramter
 * @b: second parameter
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function
 * @a: first parameter
 * @b: second parameter
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function
 * @a: first parameter
 * @b: second parameter
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}

